package constructor;

public class Constructor_With_Super_KeyWord_Parent{
	
	public Constructor_With_Super_KeyWord_Parent() {
		System.out.println("Parent class constructor");
	}
	
	public Constructor_With_Super_KeyWord_Parent(int i) {
		System.out.println("Parent class constructor with value of i "+i);
	}
	public Constructor_With_Super_KeyWord_Parent(int j, int k) {
		System.out.println("Parent class constructor with value of j and k "+j+" "+k);
	}
}