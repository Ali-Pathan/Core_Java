package basic_java;

public class R4_Loop {

	public static void main(String[] args) {
		// While loop
		
		int i = 0;
		
		// While loop
		
		while (i <= 10) {
		  System.out.println(i);
		 //i= i+1;
		  i++;
		  
		}
		System.out.println();
		
		
		// for loop
		
		for (int j = 20; j<=30; j++) {
			System.out.println(j);

		}

	}
	
}
